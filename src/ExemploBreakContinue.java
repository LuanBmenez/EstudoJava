public class ExemploBreakContinue {
    public static void main(String[] args) {
        for (int Numero = 1;Numero <= 5; Numero++) {
            if (Numero ==3) 
            break;
            System.out.println(Numero);
            
        }
    }
}
//Break = Pause
//Continue = Pula o numero Ex if (Numero ==3) ira imprimir 1 2 4 5